export function searchEmoji(query: string) {
  const input = query.trim().toLowerCase();
  if (!input) return EMOJI;
  return EMOJI.filter(
    (emoji) =>
      emoji.name.toLowerCase().includes(input) ||
      emoji.keywords.some((keyword) => keyword.includes(input))
  );
}
